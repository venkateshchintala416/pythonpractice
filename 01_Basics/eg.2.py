# Datatypes
# Number
# int, float, complex, bool

# String
# str

# Sequences / collections
# list, tuple, set, frozenset, dict, range, bytes, bytearray

i = int
print(i)   # <class 'int'>

i = int()
print(i)     # 0

i = int(10)
print(i)    #10

f = float
print(f)    # <class 'float'>

f = float()
print(f)    # 0.0

f = float(10)
print(f)    # 10.0

b = bool
print(b)    # <class 'bool'>

b = bool()
print(b)   # false
print(1 * True)  # 1
print(1 * False) # 0

s = str
print(s)    # <class 'str'>

s = str()
print(s)    # --

l = list
print(l)    # <class 'list'>

l = list()
print(l)    # []

t = tuple
print(t)     # <class "tuple'>

t = tuple()
print(t)    # ()

d = dict
print(d)    # <class 'dict'>

d = dict()
print(d)    # {}

s = set
print(s)    # <class 'set'>

s = set()
print(s)    # set()