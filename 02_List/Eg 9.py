# sort()
l1 = [20, 10, 70, 60, 30, 50, 40]
l1.sort(reverse=False)
print(l1)

l2 = [20, 10, 70, 60, 30, 50, 40]
l2.sort(reverse=True)
print(l2)

l3 = ['b', 'a', 'd', 'c']
l3.sort(reverse=False)
print(l3)
l3.sort(reverse=True)
print(l3)

# chr()
print(chr(65))  # A
print(chr(78))  # N

# ord()
print(ord('A'))
print(ord('N'))